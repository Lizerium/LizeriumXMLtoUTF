DWORD CreateID(LPCSTR string)
{
	DWORD hash = 0;

	for (; *string; ++string)
	{
		hash = (hash >> 8) ^ flcrcm16tbl[ (BYTE)hash ^ (BYTE)tolower(*string) ];
	}

	// Swap the bytes around.
	hash = (hash >> 24) |
		   ((hash >> 8) & 0xFF00) |
		   ((hash & 0xFF00) << 8) |
		   (hash << 24);

	return (hash >> 2) | 0x80000000;
}
