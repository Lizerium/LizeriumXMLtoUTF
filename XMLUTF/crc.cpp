DWORD fl_crc32_b(LPCSTR string)
{
    DWORD crc = 0xFFFFFFFF;

	for (; *string; ++string)
    {
		crc = (crc >> 8) ^ flcrc32tbl[ (BYTE)crc ^ (BYTE)*string ];
    }

    return ~crc;
}


DWORD fl_crc32(LPCSTR string)
{
    DWORD crc = 0xFFFFFFFF;

	for (; *string; ++string)
    {
		crc = (crc >> 8) ^ flcrc32tbl[ (crc ^ tolower(*string)) & 0xFF ];
    }

    return ~crc;
}


